package com.infocom.model;


import java.util.Date;

import com.infocom.model.DAO.NoteDeFraisDAO;

public class NoteDeFrais{
	
	private int id;
	private String raison;
	private double prix;
	private Date date;
	
	public NoteDeFrais() {};
	
	public NoteDeFrais(int id, String raison, double prix, Date dateUpp) {
		super();
		this.id = id;
		this.raison = raison;
		this.prix = prix;
		this.date = dateUpp;
	}
	
	 public int getId() {
			return id;
		}
	 
	 public int setId(int id) {
			return this.id = id;
		}
	 
	 public String getRaison() {
		 return raison;
	 }
	 public String setRaison(String raison) {
		 return this.raison = raison;
	 }
	
	 public double getPrix() {
		 return prix;
	 }
	 public double setPrix(double prix) {
		 return this.prix = prix;
	 }
	 
	 public Date getDate() {
		 return date;
	 }
	 public Date setDate(Date date) {
		 return this.date = date;
	 }

	@Override
	public String toString() {
		return "NoteDeFrais [id=" + id + ", raison=" + raison + ", prix=" + prix
				+ ", date=" + date + "]";
	}
	 
	 
}
